package models;

public class Pet {

    private String nomePet;
    private String raca;
    private String porte;
    private String alimentação;
    private int idadePet;

    
    public String getNomePet() {
        return nomePet;
    }
    public void setNomePet(String nomePet) {
        this.nomePet = nomePet;
    }
    public String getRaca() {
        return raca;
    }
    public void setRaca(String raca) {
        this.raca = raca;
    }
    public String getPorte() {
        return porte;
    }
    public void setPorte(String porte) {
        this.porte = porte;
    }
    public String getAlimentação() {
        return alimentação;
    }
    public void setAlimentação(String alimentação) {
        this.alimentação = alimentação;
    }
    public int getIdadePet() {
        return idadePet;
    }
    public void setIdadePet(int idadePet) {
        this.idadePet = idadePet;
    }
    

}
